    <h3>Metode de plata</h3>
<h4>1. Ramburs</h4>
<p>Plateste in numerar sau prin intermediul sistemului POS (valabil pentru partenerii ADD Cargo si GLS), la livrare, prin optiunea de plata “Ramburs”.</p>
<p>Poti plati folosind aceasta metoda daca:</p>
<p>1. valoarea totala a comenzilor tale nu depaseste 1000 de lei, in cazul produselor provenite din stocul furnizorilor nostri;</p>
<p>2. valoarea totala a comenzilor tale nu depaseste 3000 de lei, in cazul produselor provenite din stocul nostru propriu, produse ce beneficiaza si de livrare rapida.</p>
<p>Nu uita sa ai in vedere taxa de 6 lei aplicata de catre compania de curierat, valabila la toate platile de tip “Ramburs”.</p>

<h4>2. Plata online cu card bancar<h4>
<p>Plateste online cu cardul bancar si beneficiezi de cea mai simpla si rapida metoda de plata!</p>
<p>Tranzactia este efectuata in siguranta cu ajutorul platformei Adyen. In plus, scapi de costurile aditionale ale serviciilor de curierat, valabile la platile in numerar.</p>
<p>In cazul produselor returnate, suma cheltuita iti este restituita direct pe card, in termen de maximum 14 zile.</p>
<p>Cardurile eligibile sunt: VISA (Classic si Electron), MASTERCARD, MAESTRO.</p>
<h4>3. Transfer bancar</h4>
<p>Prin aceasta optiune, clientii au la dispozitie 48 de ore pentru achitarea comenzilor in contul bancar aferent comerciantilor.</p>
<p>In formularul de plata se completeaza numarul comenzii in dreptul campului “Detalii plata/Explicatii”.</p>
